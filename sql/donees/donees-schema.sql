create table donees (
    donee_id int(11) not null auto_increment primary key,
    donee_type enum('Private nonprofit','Donor-advised fund','Subsidiary') default 'Private nonprofit',
    donee varchar(120) not null,
    other_names varchar(140),
    country varchar(40), # -- Country of main activity
    bay_area boolean, # -- Whether the person currently lives in the San Francisco Bay Area
    facebook_username varchar(100),
    website varchar(150),
    donate_page varchar(200),
    donor_list_page varchar(200),
    transparency_and_financials_page varchar(200),
    donation_case_page varchar(1000), # -- The most recent case(s) made for donations to the org
    donation_statistics_page varchar(1000),
    contact_email varchar(40),
    twitter_username varchar(40),
    eahub_username varchar(40),
    wikipedia_page varchar(120),
    givewell_review varchar(100),
    open_phil_grant_review varchar(200),
    ace_review varchar(200),
    key_people varchar(2000),
    cause_area varchar(2000),
    recommenders varchar(2000),
    launch_date date,
    launch_date_precision enum('day','month','year','multi-year'),
    launch_date_url varchar(200),
    notes varchar(2000) default null,
    youtube_username varchar(100),
    instagram_username varchar(100),
    tumblr_subdomain varchar(100),
    medium_username varchar(100),
    pinterest_username varchar(100),
    charity_navigator_page varchar(100),
    guidestar_page varchar(100),
    timelines_wiki_page varchar(100),
    autogenerated boolean, # -- Whether the row was inserted through an autogeneration script
    orgwatch boolean,
    unique key donee(`donee`)
) ENGINE=InnoDB AUTO_INCREMENT=15239276 DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;
